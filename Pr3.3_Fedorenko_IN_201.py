input_string = "Don't worry, it's a piece of cake, there will be no problem! Choose a job you love, and you will never have to work a day in your life!!!"

# Вивести перші 5 символів
print("Перші 5 символів:", input_string[:5])

# Вивести останні 10 символів
print("Останні 10 символів:", input_string[-10:])

# Вивести всі символи, починаючи з 10-го і до кінця
print("Символи з 10-го до кінця:", input_string[9:])

# Вивести всі символи, починаючи з 4-го і до передостаннього із кроком 3
print("Символи з 4-го до передостаннього з кроком 3:", input_string[3:-1:3])

# Вивести всі символи у зворотному порядку (починаючи із кінця)
print("Символи у зворотному порядку:", input_string[::-1])
