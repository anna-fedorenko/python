def check_password(password):
    # Ініціалізація словника для зберігання помилок
    ERR = {}

    # Перевірка довжини пароля
    if len(password) < 6:
        ERR["Довжина"] = "Пароль має містити принаймні 6 символів"
    
    # Перевірка наявності символів верхнього і нижнього регістрів
    if not any(char.islower() for char in password) or not any(char.isupper() for char in password):
        ERR["Регістр"] = "Пароль має містити як малі, так і великі літери"
    
    # Перевірка наявності цифр
    if not any(char.isdigit() for char in password):
        ERR["Цифри"] = "Пароль має містити принаймні одну цифру"
    
    # Перевірка наявності спеціальних символів
    if not any(char.isalnum() == False for char in password):
        ERR["Спеціальні символи"] = "Пароль має містити принаймні один спеціальний символ"

    # Перевірка наявності помилок
    if ERR:
        # Виведення повідомлення про помилки
        print("Пароль ненадійний. Помилки:")
        for key, value in ERR.items():
            print(f"- {key}: {value}")
    else:
        # Виведення повідомлення про припустимість пароля
        print("Пароль припустимий")

# Зчитування пароля від користувача
password = input("Введіть пароль: ")

# Виклик функції для перевірки пароля
check_password(password)
