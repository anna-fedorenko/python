# Запитуємо користувача про число n
n = int(input("Введіть число n для обчислення його факторіалу: "))

# Перевіряємо, чи є n невід'ємним цілим числом
if n < 0:
    print("Помилка: число має бути невід'ємним.")
else:
    # Ініціалізуємо змінну для факторіалу
    factorial = 1
    
    # Обчислюємо факторіал за допомогою циклу
    for i in range(1, n + 1):
        factorial *= i
    
    # Виводимо результат
    print(f"Факторіал числа {n} дорівнює {factorial}")