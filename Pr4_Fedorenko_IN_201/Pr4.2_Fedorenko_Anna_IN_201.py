import random

# Сформуємо список list_2 із 10 випадкових цілих чисел із діапазону [-7, 7]
list_2 = [random.randint(-7, 7) for _ in range(10)]
print(list_2)

# Виведемо мінімальний та максимальний елементи списку
print("Мінімальний елемент списку:", min(list_2))
print("Максимальний елемент списку:", max(list_2))

# Знайдемо довжину списку та суму його елементів
print("Довжина списку:", len(list_2))
print("Сума елементів списку:", sum(list_2))

# Знайдемо індекси мінімального та максимального елементів
min_index = list_2.index(min(list_2))
max_index = list_2.index(max(list_2))
print("Індекс мінімального елемента:", min_index)
print("Індекс максимального елемента:", max_index)

# Поміняємо місцями максимальний та мінімальний елементи
list_2[min_index], list_2[max_index] = list_2[max_index], list_2[min_index]
print("Список після обміну місцями максимального та мінімального елементів:", list_2)

# Виведемо відсортовану копію списку
sorted_list = sorted(list_2)
print("Відсортована копія списку:", sorted_list)
